def get_glossary_explanation(term: str) -> str | None:
    """获取企业术语解释"""
    return ENTERPRISE_GLOSSARY.get(term.upper()) or ENTERPRISE_GLOSSARY.get(term)


def build_system_prompt(module: str = "general") -> str:
    """
    构建完整的系统提示词（基础人设 + 模块人设 + 输出规范 + 术语表摘要）

    Args:
        module: 模块名 (policy/tech/admin/general)

    Returns:
        完整的 system prompt 字符串
    """
    parts = [BASE_PERSONA.strip()]

    # 添加模块专属人设
    if module in MODULE_PERSONAS:
        parts.append(MODULE_PERSONAS[module].strip())

    # 添加输出格式规范
    parts.append(OUTPUT_FORMAT_GUIDELINES.strip())

    # 添加常用术语提示（取前20个最常用的，避免prompt过长）
    top_terms = list(ENTERPRISE_GLOSSARY.items())[:20]
    glossary_text = "\n".join([f"- {term}: {desc}" for term, desc in top_terms])
    parts.append(f"【企业常用术语参考】\n{glossary_text}\n")

    return "\n\n".join(parts)


def build_few_shot_messages(module: str = "general") -> list[dict]:
    """
    构建少样本示例消息列表

    Args:
        module: 模块名

    Returns:
        少样本消息列表 [{"role": "user", "content": ...}, {"role": "assistant", "content": ...}]
    """
    examples = FEW_SHOT_EXAMPLES.get(module, [])
    messages = []
    for ex in examples[:2]:  # 每个模块最多2个示例，避免过长
        messages.append({"role": "user", "content": ex["question"]})
        messages.append({"role": "assistant", "content": ex["answer"].strip()})
    return messages


@dataclass
class PersonaConfig:
    """人设配置类"""
    module: str = "general"
    temperature: float = 0.7
    top_p: float = 0.9
    max_tokens: int = 2048
    use_few_shot: bool = True

    def get_system_prompt(self) -> str:
        return build_system_prompt(self.module)

    def get_few_shot_messages(self) -> list[dict]:
        if self.use_few_shot:
            return build_few_shot_messages(self.module)
        return []
